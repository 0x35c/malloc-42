#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	char	*str;

	if (s == NULL)
		return (NULL);
	str = (char *) s;
	while (n)
	{
		*str = c;
		++str;
		n--;
	}
	return (s);
}
